package Chapter2.익명클래스예제.Apple;

public class Apple {
	Color color;
	int weight;

	public Apple(int weight, Color color) {
		this.weight = weight;
		this.color = color;
	}

	public Color getColor() {
		return color;
	}

	public void setColor(Color color) {
		this.color = color;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}
}
